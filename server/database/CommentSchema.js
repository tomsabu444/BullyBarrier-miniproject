const mongoose = require("mongoose");

const CommentSchema = new mongoose.Schema(
  {
    clerkUserId: {
      type: String,
      required: true,
    },
    fullname: {
      type: String,
      required: true,
    },
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
    categories: Object,
    flagged: {
      type: Boolean,
      default: false,
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
  },
  { versionKey: false } // __v key hide
);

module.exports = mongoose.model("comments", CommentSchema);
